<h3><a name="allergies" id="allergies"></a>Allergies</h3>

<p>Allergies are an immune response triggered by allergens, an ordinarily harmful substance.</p>

<h3>Causes</h4>

<p>People with allergies have especially sensitive immune systems that react when they contact allergens.</p>

<p>Common allergens include:</p>

<ul><li  class="font">foods (nuts, eggs, milk, soy, shellfish, wheat)</li>
	<li  class="font">pollen</li>
	<li  class="font">mold</li>
	<li  class="font">latex</li>
	<li  class="font">pet dander</li>
</ul><h3>Symptoms</h4>

<p>Because there are so many possible causes, the symptoms of allergies vary widely. Airborne allergens, like pollen and pet dander, are likely to cause:</p>

<ul><li  class="font">Eye irritation</li>
	<li  class="font">Runny nose</li>
	<li  class="font">Stuffy nose</li>
	<li  class="font">Puffy, watery eyes</li>
	<li  class="font">Sneezing</li>
	<li  class="font">Inflamed, itchy nose and throat</li>
</ul><p>Allergens that are consumed, like foods or certain medications, can cause:</p>

<ul><li  class="font">Hives or skin rashes</li>
	<li  class="font">Gastrointestinal distress (diarrhea, nausea, vomiting, excessing gas, indigestion)</li>
	<li  class="font">Tingling or swelling of the lips, face, or tongue</li>
	<li  class="font">Itchiness</li>
	<li  class="font">Difficulty breathing or wheezing</li>
	<li  class="font">Fainting/ or lightheadedness</li>
</ul><p>In cases of a more extreme response, called anaphylaxis, symptoms are severe and life-threatening.</p>

<h3>Treatment</h4>

<p>The easiest and most effective way to treat allergies is to get rid of or avoid the cause. Where unavoidable, some lifestyle changes can reduce your allergy symptoms. For example, if you are allergic to dust mites, make an effort to keep your room clean and free of dust by frequent vacuuming, dusting, and washing of bedding.</p>

<p>For pollen allergies, avoid being outside when pollen counts are high and keep the windows to your room shut.</p>

<p>Because it is very difficult to avoid certain allergens, medication may be necessary to lessen symptoms caused by allergens, other than food and drugs.</p>

<ul><li  class="font"><strong>Antihistamines</strong>: help relieve or prevent the sneezing, itchy eyes and throat, and postnasal drip that the allergen may cause.&nbsp;They are sold in many forms (i.e., pills, nasal sprays, liquids, etc.).</li>
	<li  class="font"><strong>Decongestants:</strong>&nbsp;help reduce congestion in your nasal membranes by narrowing the blood vessels that supply those membranes. They can be purchased in several forms (liquid, pill or nasal spray) and may be used with an antihistamine or alone to treat nasal swelling related to allergies. &nbsp;Limit use of nasal sprays to fewer than two to three days in a row because prolonged use can cause the nasal membrane swells, resulting in severe nasal obstruction.&nbsp;</li>
	<li  class="font"><strong>Anti-inflammatory agents (e.g., corticosteroid):</strong> help reduce swelling of the airways, nasal congestion and sneezing. Typically taken as a nasal spray. Some people report that corticosteroids irritate nasal passages.</li>
	<li class="font"><strong>Allergy shots:</strong> recommended for serious allergy sufferers, this series of shots are administered by a healthcare provider and contain small amounts of the allergens that cause you discomfort. The goal of allergy shots is to enable your immune system to build better defenses against allergens.</li>
</ul><p>Some allergies go away with age, but others are lifelong.</p>

<h3>Prevention</h4>

<ul><li  class="font">Avoid the outdoors between 5-10 a.m. and save outside activities for late afternoon or after a heavy rain, when pollen levels are lower.</li>
	<li  class="font">Keep windows in your living spaces closed to lower exposure to pollen.</li>
	<li  class="font">To keep cool, use air conditioners and avoid using window and attic fans.</li>
	<li  class="font">Wear a medical alert bracelet or other means to communicate to others about your allergy in case of a reaction.</li>
	<li  class="font">Discuss a prescription for epinephrine (e.g., EpiPen) with your healthcare provider, if you have risk of serious allergic reaction.</li>
	<li  class="font">Review product labels carefully before buying or consuming any item</li>
	<li  class="font">Know what you are eating or drinking.</li>
</ul><h3><a name="coldflu" id="coldflu"></a>Cold and Flu</h3>

<p>Colds and influenza (flu)&nbsp;are&nbsp;the most common illnesses among college students.&nbsp;</p>

<h3>Causes</h4>

<p>Both of these illnesses are upper respiratory infections, meaning they involve&nbsp;your nose, throat, and lungs. Viruses cause both colds and flu&nbsp;by increasing&nbsp;inflammation of the membranes in the nose and throat.</p>

<p>Most transmission of these viruses occurs via hand-to-hand contact.</p>

<h3>Symptoms</h4>

<p>Flu symptoms come on suddenly and affect the body all over. Flu symptoms are usually more serious than a cold and&nbsp;include:</p>

<ul><li  class="font">fever (100°&nbsp;F),</li>
	<li  class="font">headache,</li>
	<li  class="font">more intense pain and fatigue, and</li>
	<li  class="font">more severe, often&nbsp;dry cough.</li>
</ul><p>When you get the flu, you are also more prone to bronchitis, sinus, and ear infections.&nbsp;</p>

<p>Cold symptoms mostly affect above the neck and include:</p>

<ul><li  class="font">a runny or stuffy nose (nasal congestion),</li>
	<li  class="font">sneezing,</li>
	<li  class="font">sore throat, and</li>
	<li  class="font">cough.&nbsp;</li>
</ul><p>You may also experience a mild headache, body aches or a low grade fever. Typically, a cold lasts&nbsp;2-14 days.</p>

<h3>Treatment</h4>

<p>If any problem is causing you discomfort, you should seek medical care.</p>

<p>Seek medical attention promptly if you have:</p>

<ul><li  class="font">a fever of 102°&nbsp;F or greater (which may indicate a more serious infection),</li>
	<li  class="font">a persisting cough, especially with a significant fever (which could indicate pneumonia),</li>
	<li  class="font">a persistent sore throat (especially if runny nose does not develop - which could indicate a strep infection), or</li>
	<li  class="font">any cold lasting more than 10 days.</li>
</ul><p>Because colds and flu are caused by viruses, they cannot be cured by antibiotics. There are tips to help you feel better and&nbsp;strengthen your immune system to fight illness:</p>

<ul><li  class="font">Rest more than usual and avoid exercise until symptoms are gone.</li>
	<li  class="font">Drink lots of clear fluids (e.g., water, tea).</li>
	<li  class="font">Stay away from cigarette smoke.</li>
	<li  class="font">Do not take antibiotics unless specifically prescribed for you to cure the illness from which you currently suffer.</li>
	<li  class="font">Avoid drinking alcohol because it weakens your immune system and may interact with medications.</li>
	<li  class="font">Avoid caffeine, which can increase congestion and dehydration.</li>
	<li  class="font">Eat a well-balanced diet, including fruits, vegetables, and grains.</li>
</ul><h3>More Specific Remedies for Comfort</h4>

<h3>Runny Nose/Congestion:</h5>

<ul><li  class="font">Decongestants (e.g. pseudoephedrine) can relieve a runny nose and congestion, but these medications can inhibit sleep and suppress appetite.</li>
	<li  class="font">Salt water nasal sprays (e.g. NaSal or Ocean) can ease nasal congestion and thin mucus. However, excessive use of medicated nose sprays, like Afrin, can cause dependence and may make congestion worse.</li>
	<li class="font">Humidifiers and hot showers can help to moisten nasal passages and clear mucus.</li>
</ul><h3>Cough:</h5>

<ul><li class="font">Dextromethorphan is an effective cough suppressant, but because a cough is a protective reflex, it is not usually a bad thing. Take dextromethorphan if your cough is interfering with sleep or work.</li>
	<li class="font">Water vapor from humidifiers and showers can help loosen the mucus causing a cough, as can chicken soup.</li>
</ul><h3>Sore Throat:</h5>

<ul><li class="font">Phenol in lozenges and sprays is an effective pain reliever for sore throats.</li>
	<li class="font">Gargling with warm saltwater (1 tsp. salt in one cup of warm water) every four hours may help ease pain by reducing swollen tonsils.</li>
	<li class="font">Drinking tea with lemon (with or without honey).</li>
</ul><h3>Fever/Pain:</h5>

<ul><li class="font">Acetaminophen, Aspirin, Ibuprofen. If symptoms are severe, you may alternate acetaminophen and ibuprofen every two hours for pain or fever relief.</li>
</ul><h3>Prevention</h4>

<p>UHS offers flu shots to Princeton students at a reduced cost every fall. Even though getting a flu shot will not completely eliminate your chances of developing the flu, it will certainly reduce the risk. Each year, a new vaccine made from inactivated (killed) influenza viruses is formulated. Since it may take the immune system time to respond to the vaccination, the inactivated vaccine should be given 6 to 8 weeks before flu season begins in order to prevent infection or reduce the severity of the illness.</p>

<p>The flu is probably only contagious during the first three days of illness, and the incubation period is 24-72 hours, meaning you might not show symptoms for three days after contracting the virus. It is rare to catch a cold virus through the air – most transmission occurs via hand-to-hand contact. To prevent colds, flu, and other illnesses, follow these tips:</p>

<ul><li class="font">Wash your hands often (which is good advice for keeping healthy in any situation). Keep them away from your nose, eyes, and mouth. Use an instant hand sanitizer when you can’t wash your hands.</li>
	<li class="font">Get regular exercise and eat well.</li>
	<li class="font">Follow good sleep habits.</li>
	<li class="font">Get a flu shot each fall (offered to all students at a lower cost by UHS each fall)&nbsp;</li>
</ul><h3><a name="conjunctivitis" id="conjunctivitis"></a>Conjunctivitis (“pink eye“)</h3>

<h3>Causes</h4>

<p>Conjunctivitis, an inflammation of the transparent membrane (conjunctiva) that lines your eyelids and part of your eyeballs, has several possible causes. It could be a bacterial or viral infection, an allergic reaction to pollen or animal dander, or a result of chemical irritants (smoke, chlorine, lens solution, etc.).</p>

<h3>Symptoms</h4>

<p>These symptoms may last a few hours to several weeks: redness, itching, tearing, burning sensation, pus-like discharge and/or crusting of the eyelids. Because conjunctivitis causes inflammation of the small blood vessels in the conjunctiva to become more prominent, the whites of your eyes will appear pink or red. When you wake you are likely to feel that your eyelids are pasted shut, and your vision may not be as clear as usual.</p>

<h3>Treatment</h4>

<p>Because pink eye is highly contagious, early diagnosis is important. Bacterial cases can be cured with antibiotic eye drops, viral conjunctivitis clears up on its own, and allergic reactions can be treated with various types of eye drops. Here are some general tips:</p>

<ul><li class="font">Wash your hands frequently to prevent spreading an existing infection to your other eye, and to other people.</li>
	<li class="font">Don’t rub your eyes.</li>
	<li class="font">Use a cool wet washcloth to soak off any crusting.</li>
	<li class="font">Use a warm or cool compress to reduce discomfort.</li>
	<li class="font">Discard eye make-up because it may cause future infection.</li>
	<li class="font">Wash any clothing that may be contaminated, including towels and pillowcases. Try to use clean towels and pillowcases everyday.</li>
	<li class="font">Avoid wearing contact lenses and discard current lenses.</li>
	<li class="font">If eye drops are prescribed, place drop in pocket formed by pulling down lower lid. Make sure you don’t touch the bottle to the eye in order to prevent contamination.</li>
	<li class="font">If the infection does not improve in 2 or 3 days, make an appointment for re-evaluation.</li>
</ul><h3>Prevention</h4>

<p>Pink eye is extremely contagious, so if you know someone who is infected, make sure you do not expose yourself to contact with the person’s eye fluid. The infection can easily be passed via keyboards, doorknobs, make-up, pens, gym equipment, and a vast number of other items. To protect yourself, wash your hands frequently, avoid touching your eyes, and wash anything that may be contaminated.</p>

<h3><a name="diarrhea" id="diarrhea"></a>Diarrhea</h3>

<h3>Causes</h4>

<ul><li  class="font">Bacterial infection, caused by contaminated food or water</li>
	<li class="font">Viral infection</li>
	<li class="font">Parasites, which can enter the body through food or water</li>
	<li class="font">Food intolerance, such as the inability to digest lactose, the sugar in milk</li>
	<li class="font">Overuse of alcohol or laxatives</li>
	<li  class="font">Medication, such as some antibiotics or antacids containing magnesium</li>
	<li class="font">Menstrual cramps</li>
	<li class="font">Stress or a panic attack</li>
</ul><h3>Symptoms</h4>

<ul><li  class="font">watery, loose stools</li>
	<li class="font">frequent bowel movements</li>
	<li class="font">cramping or pain in the abdomen, nausea, bloating</li>
	<li class="font">possibly fever or bloody stools, depending on the cause</li>
</ul><h3>Treatment</h4>

<p>Usually diarrhea will clear up on its own in a day or two, but a prolonged case may cause complications. The most important concern is dehydration. If you have symptoms of dehydration, a fever above 102° F, bloody stools (black and tarry), severe abdomen or rectum pain, or diarrhea lasting more than 3 days you should consult a physician. Here’s some advice for taking care of diarrhea yourself:</p>

<ul><li  class="font">Avoid foods that are milk-based, greasy, high-fiber, or very sweet because these are likely to aggravate diarrhea.</li>
	<li  class="font">Avoid caffeine and alcohol.</li>
	<li  class="font">Do not eat solid food if you have signs of dehydration (thirst, light-headed, dark urine). Instead, drink about 2 cups of clear fluids per hour (if vomiting isn’t present), such as sports drinks and broth. Water alone is not enough because your body needs sodium and sugar to replace what it’s losing.</li>
	<li  class="font">Avoid high sugar drinks, like apple juice, grape juice, and soda, which can pull water into the intestine and make the diarrhea persist.</li>
	<li  class="font">Don’t drink clear liquids exclusively for more than 24 hours.</li>
	<li  class="font">Begin eating normal meals within 12 hours, but stick to food that is bland and won’t irritate your intestine. Some doctors suggest the “BRAT“ diet which includes foods that are low in fiber, fat, and sugar. BRAT stands for&nbsp;Bananas,&nbsp;Rice,&nbsp;Applesauce, and&nbsp;Toast.</li>
	<li  class="font">Use over-the-counter lactobacillus acidophilus capsules or tablets. These bacteria help maintain a healthy intestine, and are found in yogurt with live active cultures.</li>
	<li  class="font">Decrease level of exercise until symptoms are gone.</li>
	<li  class="font">Over-the-counter drugs, such as Imodium A-D, should only be used if absolutely necessary because it is important to let diarrhea flush out the bacteria or parasite that’s causing the infection.</li>
</ul><h3><a name="headaches" id="headaches"></a>Headaches</h3>

<p>Everyone suffers the occasional mild headache, but if you experience debilitating pain and/or abnormally frequent headaches, you probably want to find relief. There are countless causes of headaches, which differ for each person, so you’ll have to do some experimenting to figure out the cause of your pain. Fortunately, the vast majority of headaches are primary headaches, not the result of underlying medical conditions. The three most common types are cluster, tension-type, and migraine.</p>

<p>A cluster headache affects a specific point of the head, often the eye, and is characterized by sharp, piercing pain. Migraine and tension-type headaches are far more common. “Tension“ headaches are now called “tension-type“ headaches because pain is not only caused by stress, but also poor posture, depression, and even sexual activity. In fact, recent studies have shown a connection between low serotonin levels and so-called “tension“ headaches.</p>

<h3>Causes</h4>

<p>The cause of a headache is often elusive. Although it may seem that your head is pounding for no reason, there is always an explanation for pain. To find out the cause of your headaches, keep a log. Write down the date and time each headache starts and stops, the location of the pain, the nature and severity of the pain, and any factors that seem to trigger the headaches (food, stress, menstrual cycle, medicine, etc.).</p>

<p>Some of the many causes of headaches:</p>

<ul><li class="font">Emotional and physical stress</li>
	<li class="font">Fatigue</li>
	<li class="font">Irregular sleep habits (sleeping too much or too little)</li>
	<li class="font">Skipping meals</li>
	<li class="font">Caffeine use or withdrawal</li>
	<li class="font">Hormonal factors, such as menstruation</li>
	<li class="font">Monosodium glutamate (MSG)</li>
	<li class="font">Foods with nitrates, such as hot dogs</li>
	<li class="font">Alcohol</li>
	<li class="font">Some medicines</li>
	<li class="font">Certain foods, including red wine, chocolate, aged cheeses, pickled foods, nuts, and aspartame</li>
	<li class="font">Changes in weather, altitude, or time zone</li>
</ul><h3>Symptoms</h4>

<p>Just as the causes vary for each headache sufferer, so do the symptoms and severity of pain. Health professionals can often diagnose the type of headache you suffer based on your symptoms.</p>

<p>Symptoms of a migraine:</p>

<ul><li  class="font">pulsing or throbbing quality</li>
	<li class="font">begins with intense pain on one side of the head, which eventually spreads</li>
	<li class="font">felt on one or both sides of the head</li>
	<li class="font">lasts several hours</li>
	<li class="font">severe enough to interfere with routine activities</li>
	<li class="font">may be accompanied by nausea or vomiting</li>
	<li class="font">sometimes preceded by visual changes, such as an aura of zigzag lines or flashes of light</li>
	<li class="font">light and noise can make the headache worse, while sleep tends to relieve symptoms</li>
</ul><p>Symptoms of a tension-type headache:</p>

<ul><li class="font">constant, dull ache</li>
	<li class="font">felt on both sides of the head</li>
	<li class="font">a feeling of squeezing or pressure</li>
	<li class="font">does not usually interfere with routine activities</li>
	<li class="font">lasts from 30 minutes to a few days</li>
</ul><h3>Treatment</h4>

<ul><li class="font">Ice pack held over the eyes or forehead</li>
	<li class="font">Heating pad set on low or hot shower to relax tense neck and shoulder muscles</li>
	<li class="font">Sleep, or at least resting in a dark room</li>
	<li class="font">Taking breaks from stressful situations</li>
	<li class="font">Regular exercise to increase endorphin levels and relax muscles. Even if you already have a headache, exercising may relieve the pain. However, intense exercise may bring on a headache.</li>
	<li class="font">Occasional use of over-the-counter medicines such as acetaminophen, ibuprofen, or aspirin can relieve both migraine and tension headaches. *</li>
	<li class="font">Prescription drugs for severe headaches</li>
</ul><p>* Overuse of pain medicine can actually result in more frequent headaches. Most pain-relieving medicines used to treat headaches can cause “analgesic rebound headaches“ if used too often.</p>

<h3>Prevention</h4>

<ul><li class="font">Be aware of early symptoms so you can try to stop the headache as soon as it begins.</li>
	<li class="font">Don’t smoke, and if you do, quit.</li>
	<li class="font">Don’t skip meals.</li>
	<li class="font">Cut down on caffeine and alcohol (reduce caffeine intake gradually because withdrawal may cause headaches).</li>
	<li class="font">Stop all over-the-counter medicines and herbal remedies.</li>
	<li class="font">Maintain a regular eating and sleeping schedule.</li>
	<li class="font">Exercise regularly.</li>
	<li class="font">Incorporate relaxation activities into your daily routine, such as meditation, yoga, stretching exercises, and massage</li>
	<li class="font">Improve your posture, possibly by adjusting your workstation.</li>
</ul><h3>When should I see a health care provider?</h5>

<p>Although most headaches do not signal underlying medical problems, if your headaches change in nature or location, or become more severe, frequent, and/or intense, you should consult your health care provider.</p>

<p>Seek immediate medical attention if your headaches…</p>

<ul><li class="font">Become increasingly worse, and are most painful early in the morning while you are still in bed.</li>
	<li class="font">Occur three or more times a week.</li>
	<li class="font">Begin to be accompanied by nausea or vomiting.</li>
	<li class="font">Occur after a head injury, especially if they get worse.</li>
	<li class="font">Seem to be triggered by physical exertion, such as exercise, sexual activity, or coughing.</li>
	<li class="font">Occur along with green or brown nasal drainage and sinus pain over the cheeks or around the eyes.</li>
	<li class="font">Are accompanied by dizziness, difficulty speaking, vision loss, or numbness in an arm, leg, or parts of the face.</li>
	<li class="font">Develop suddenly, accompanied by a fever over 101°F and are not relieved by acetaminophen or aspirin, especially if there is pain or stiffness in the neck.</li>
</ul><h3><a name="mono" id="mono"></a>Mononucleosis</h3>

<h3>Cause</h4>

<p>Mononucleosis is an illness caused by the Epstein-Barr virus (EBV), which is spread through saliva.</p>

<h3>Symptoms</h4>

<p>The most well known symptom of “mono“ is extreme fatigue, forcing the infected person to nap frequently. If you experience such extreme fatigue accompanied by other symptoms, such as swollen lymph glands and spleen, sore throat, fever, loss of appetite, and muscle aches, you may want to get tested for mono. The basis for testing whether you have mono is the presence of antibodies produced by white blood cells. Many people infected with mono don’t get sick, or have such mild symptoms they don’t know they have it. EBV is usually in the body 30-50 days before an infected person develops symptoms. Surprisingly, 80-95% of adults in the US have been infected by the time they’re 40, but only about 20% know they’ve had mono.</p>

<h3>Treatment</h4>

<p>Mono is a virus, so antibiotics won’t help. Make sure you get plenty of rest, eat healthy foods, avoid alcohol (because your liver may be inflamed and drinking weakens immune responses), drink plenty of fluids, take aspirin or an aspirin substitute to reduce pain and fever, gargle salt water to relieve sore throat, and avoid strenuous activity. Because your spleen may be swollen, it is important not to engage in contact sports which could rupture your spleen. Returning to normal activity too quickly increases your chances of relapse.</p>

<h3>Prevention</h4>

<p>Avoiding someone with mono can be hard because infected individuals often do not show symptoms. Because the incubation period is so long, a person can be contagious 1-2 months before showing any symptoms, and some people don’t show symptoms at all. Even after signs of mono have disappeared, a person may still be producing the virus. A strong immune system, maintained by healthy diet, exercise, and adequate sleep, can help you from getting ill. Fortunately, mono is not very contagious, and is usually only passed through intimate contact, such as kissing.&nbsp;[top]</p>

<h3><a name="stomachaches" id="stomachaches"></a>Stomach Aches</h3>

<h3>Intestinal Gas</h4>

<p>It’s natural to have gas in your digestive tract, and to pass gas regularly. In fact, everyone passes gas at least 12 times a day, which is a combination of oxygen, hydrogen, nitrogen, carbon dioxide, and methane. The unpleasant odor of flatus is cause by gases (e.g. hydrogen sulfide) produced by the bacteria in the large intestine. Even though it’s natural to have gas in your digestive tract, excessive gas can cause intense, temporary pain. People who suffer discomfort from gas in the intestine report abdominal bloating, flatulence, and jabbing pains or cramps in the abdomen. Intestinal gas can mimic the pain associated with real illnesses, like gallbladder disease, heart disease, and appendicitis, but be aware that it can also signal other illnesses, such as stomach flu or food poisoning.</p>

<p>The best treatment for gas focuses on the cause. Although there are several over-the-counter medications that advertise gas relief, they do not work for everyone and may have side effects. Medications that have been shown to effectively reduce gas use simethicone, a substance that helps gas be expelled through belching or passing flatus (it does not make gas dissolve or disappear). Activated charcoal preparations may also be helpful. While these medications are good for short-term immediate relief, the best step for long-term comfort is to find out what causes you gas.</p>

<p>There are two general causes of gas: swallowed air and the breakdown of undigested foods by harmless bacteria in the large intestine. Everyone swallows air when they eat and drink, but these activities are likely to increase the amount ingested:</p>

<ul><li  class="font">Eating or drinking rapidly</li>
	<li class="font">Drinking through a straw</li>
	<li class="font">Chewing gum or eating hard candy</li>
	<li class="font">Smoking</li>
</ul><p>Food triggers vary from person to person, but the types of food that seem to cause the most problems are those that are high in fiber or lactose. Foods that may cause gas include:</p>

<ul><li class="font">Beans</li>
	<li class="font">Peas</li>
	<li class="font">Lentils</li>
	<li class="font">Cabbage</li>
	<li class="font">Broccoli</li>
	<li class="font">Brussels sprouts</li>
	<li class="font">Milk products (if you lack adequate amounts of the enzyme lactase)</li>
	<li class="font">Onions</li>
	<li class="font">Wheat</li>
	<li class="font">Carbonated beverages</li>
	<li class="font">Sorbitol (artificial sweetener) and fructose (found in corn syrup)</li>
</ul><p>To prevent gas:</p>

<ul><li class="font">Avoid foods that trigger gas.</li>
	<li class="font">Avoid swallowing excess air by not chewing gum or eating hard candy.</li>
	<li class="font">Take digestive enzyme supplements, such as Beano (for high-fiber foods) and lactase supplements (for milk products).</li>
	<li class="font">Eat only small amounts of dairy products with food, if you suspect lactose intolerance.</li>
	<li class="font">Eat several small meals throughout the day instead of two or three larger ones.</li>
	<li class="font">Eat slowly, in a relaxed setting, and chew your food thoroughly.</li>
	<li class="font">Take a stroll after meals. Don’t sit in a slumped position or lie down after eating.</li>
	<li class="font">If increasing the fiber in your diet, do so gradually</li>
	<li class="font">Exercise to facilitate the passage of gas through the digestive tract.</li>
</ul><h3>Nausea and Vomiting</h4>

<p>Possible causes for nausea and/or vomiting:</p>

<ul><li class="font">Viruses in the intestines (viral gastroenteritis, see below)</li>
	<li class="font">Some medications, such as certain antibiotics and birth control pills</li>
	<li class="font">Eating too much or eating spoiled food</li>
	<li class="font">Drinking too much (e.g. alcohol)</li>
	<li class="font">Motion sickness</li>
	<li class="font">Morning sickness in pregnant females</li>
</ul><p>Medical conditions that cause vomiting:</p>

<ul><li class="font">Labrynthitis – inflammation of an area in the ear that usually results from an upper respiratory infection</li>
	<li class="font">Concussion from a head injury</li>
	<li class="font">Stomach ulcers</li>
	<li class="font">Hepatitis (inflammation of the liver)</li>
	<li class="font">Meningitis (inflammation of the membranes that cover the brain and spinal cord)</li>
</ul><p>Gastroenteritis, a.k.a. “stomach flu,“ is an inflammation of the intestines that can be caused by viruses, parasites, bacteria, food allergies, overuse of alcohol, and psychological reactions. Viruses that cause gastroenteritis can enter the intestine from contaminated food or water, or through contact with an infected person. Many different viruses can infect the intestine, but the signs and symptoms of gastroenteritis are generally the same: abdominal cramps, watery diarrhea and nausea and/or vomiting. Symptoms of gastroenteritis can develop anywhere from a few hours to a few days after contamination. Most cases resolve within a couple days without specific treatment, but some may last for up to 10 days. In addition to avoiding food and water that might be contaminated, you should wash your hands thoroughly and frequently (especially because viruses often enter the body through the fecal-oral route, when people do not wash their hands thoroughly after bowel movements). Because stomach flu is contagious, avoid sharing eating utensils, drinks, towels, and other objects that may transmit the virus from one person to another.</p>

<p>The biggest health risk posed by gastroenteritis is dehydration. If you exhibit signs of dehydration, including extreme thirst, dry mouth, dark urine, dizziness, and severe weakness, get medical help immediately. If you cannot replace lost fluids through sipping clear liquids, you may need intravenous fluids and hospitalization.</p>

<p>Self-care tips for vomiting:</p>

<ul><li class="font">Wait 30-60 minutes after vomiting before drinking anything to let your stomach settle.</li>
	<li class="font">Don’t eat solid foods. Don’t drink milk.</li>
	<li class="font">Drink clear liquids, taking small sips. Stir any carbonated beverages to get all the bubbles out before sipping them. Suck on ice chips if nothing else will stay down.</li>
	<li class="font">Gradually return to regular diet, but wait about 8 hours from the last time you vomited. Start with foods like dry toast, crackers, rice, and other foods that are easy to digest.</li>
	<li class="font">Avoid substances that irritate the stomach, like alcohol, aspirin, and fried foods.</li>
	<li class="font">Avoid diuretics, like caffeine and alcohol, that contribute to fluid loss.</li>
</ul><p>For nausea without vomiting:</p>

<ul><li class="font">Drink clear liquids. Eat small amounts of dry foods, such as soda crackers, if tolerated.</li>
	<li class="font">Avoid things that irritate the stomach, such as alcohol, aspirin, spicy, and fried foods.</li>
	<li class="font">For motion sickness, use an over-the-counter anti-nausea medicine, such as Dramamine.</li>
</ul><h3>When should I see a health care provider?</h5>

<ul><li class="font">If you can’t keep liquids down for more than 24 hours</li>
	<li class="font">If you have blood in vomit or stools</li>
	<li class="font">If you’re vomiting for more than 1 day</li>
	<li class="font">If you have signs of dehydration (dry mouth, dark urine, dizziness, etc.)</li>
	<li class="font">If you have a fever of more than 100° F</li>
	<li class="font">If vomiting is accompanied by a sore neck and severe headache</li>
